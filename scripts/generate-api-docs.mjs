/**
 * Regenerates the API reference from the OpenAPI specs in openapi/.
 *
 * Runs automatically at build time (imported by next.config.mjs) and manually
 * via `pnpm generate:api`. The committed spec files are the source of truth:
 *
 *   - every `openapi/<id>.json` becomes reference pages — one MDX page per
 *     operation, grouped by tag, with sidebar meta and an index page
 *   - one spec  → pages live directly under content/docs/api-reference
 *     many specs → one section per spec (api-reference/<id>/…)
 *   - `src/lib/openapi-specs.generated.json` is (re)written so the specs are
 *     bundled into the Worker for the interactive playground (no runtime fs)
 *
 * content/docs/api-reference is fully owned by this script: it is wiped and
 * rewritten on every run, so a spec deleted from openapi/ disappears from the
 * site on the next build. Never edit those pages by hand — edit the spec.
 */
import { generateFiles } from 'fumadocs-openapi';
import { createOpenAPI } from 'fumadocs-openapi/server';
import { mkdir, readdir, readFile, rm, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const root = fileURLToPath(new URL('..', import.meta.url));
const SPECS_DIR = path.join(root, 'openapi');
const OUT_DIR = path.join(root, 'content', 'docs', 'api-reference');
const REGISTRY = path.join(root, 'src', 'lib', 'openapi-specs.generated.json');

// JSX attribute value as an expression ({"…"}) so quotes/backslashes in the
// text stay valid — plain "…" attributes don't support escapes in MDX.
const q = (s) => `{${JSON.stringify(String(s ?? ''))}}`;

const tagSlug = (name) => name.toLowerCase().replace(/\s+/g, '-');

// Frontmatter values must be YAML-safe (titles/descriptions often contain
// colons); JSON string encoding is valid YAML.
const yq = (s) => JSON.stringify(String(s ?? '').replace(/\n[\s\S]*/, ''));

/** Index page for one spec: a card section per tag. */
function specIndexPage(spec, entries, hrefBase, { title, description }) {
  const out = ['---', `title: ${yq(title)}`, `description: ${yq(description)}`, '---', '', '{/* Generated by scripts/generate-api-docs.mjs — edit the spec in openapi/, not this file. */}'];
  if (spec.doc.info?.description) out.push('', spec.doc.info.description);
  for (const entry of entries ?? []) {
    if (entry.type !== 'group') continue;
    const tag = (spec.doc.tags ?? []).find((t) => tagSlug(t.name) === entry.path.toLowerCase());
    out.push('', `## ${tag?.name ?? entry.title ?? entry.path}`);
    if (tag?.description) out.push('', tag.description);
    out.push('', '<Cards>');
    for (const child of entry.entries) {
      if (child.type === 'group') continue;
      const href = `${hrefBase}/${child.path.replace(/\.mdx$/, '')}`;
      // Card descriptions are plain text — keep the first sentence, drop markdown syntax.
      const firstSentence = (child.info.description ?? '').split(/(?<=\.)\s/)[0].replace(/[`*]/g, '');
      const desc = firstSentence ? ` description=${q(firstSentence)}` : '';
      out.push(`  <Card href="${href}" title=${q(child.info.title)}${desc} />`);
    }
    out.push('</Cards>');
  }
  out.push('');
  return out.join('\n');
}

/** Root index when several specs are configured: a card per spec. */
function multiSpecRootIndex(specs) {
  const out = [
    '---',
    'title: "API Reference"',
    'description: "API documentation generated from the OpenAPI specs in openapi/."',
    '---',
    '',
    '{/* Generated by scripts/generate-api-docs.mjs — edit the specs in openapi/, not this file. */}',
    '',
    '<Cards>',
  ];
  for (const spec of specs) {
    const desc = (spec.doc.info?.description ?? '').split(/(?<=\.)\s/)[0].replace(/[`*]/g, '');
    out.push(`  <Card href="/docs/api-reference/${spec.id}" title=${q(spec.doc.info?.title ?? spec.id)}${desc ? ` description=${q(desc)}` : ''} />`);
  }
  out.push('</Cards>', '');
  return out.join('\n');
}

async function generateSpec(spec, outDir, hrefBase, index) {
  await generateFiles({
    input: createOpenAPI({ input: { [spec.id]: spec.doc } }),
    output: outDir,
    per: 'operation',
    groupBy: 'tag',
    meta: true,
    // Render operation descriptions inside the page body (as markdown) instead
    // of the plain-text frontmatter description.
    includeDescription: true,
    beforeWrite(files) {
      // Tag folders otherwise get titles inferred from their slug ("ai" → "A I").
      for (const file of files) {
        const m = file.path.match(/^([^/]+)\/meta\.json$/);
        const tag = m && (spec.doc.tags ?? []).find((t) => tagSlug(t.name) === m[1]);
        if (tag) {
          file.content = JSON.stringify({ ...JSON.parse(file.content), title: tag.name }, null, 2);
        }
      }
      files.push({ path: 'index.mdx', content: specIndexPage(spec, this.generatedEntries[spec.id], hrefBase, index) });
    },
  });
}

export async function generateApiDocs({ log = console.log } = {}) {
  let names = [];
  try {
    names = (await readdir(SPECS_DIR)).filter((n) => n.endsWith('.json')).sort();
  } catch {
    // no openapi/ directory — treat as zero specs
  }
  const specs = [];
  for (const name of names) {
    const doc = JSON.parse(await readFile(path.join(SPECS_DIR, name), 'utf8'));
    specs.push({ id: name.replace(/\.json$/, ''), doc });
  }

  // The Worker-bundled registry powering the interactive playground.
  await writeFile(REGISTRY, `${JSON.stringify(Object.fromEntries(specs.map((s) => [s.id, s.doc])))}\n`);

  // This directory is fully generated — rebuild it from scratch.
  await rm(OUT_DIR, { recursive: true, force: true });
  await mkdir(OUT_DIR, { recursive: true });

  if (specs.length === 0) {
    await writeFile(
      path.join(OUT_DIR, 'index.mdx'),
      '---\ntitle: API Reference\ndescription: No API specs configured yet.\n---\n\nAdd an OpenAPI spec to `openapi/` (or upload one from the editor sidebar) and this section is generated from it.\n',
    );
    log('API reference: no specs in openapi/ — wrote placeholder index');
    return;
  }

  if (specs.length === 1) {
    const spec = specs[0];
    const intro = spec.doc.info?.description ?? `${spec.doc.info?.title ?? spec.id} reference.`;
    await generateSpec(spec, OUT_DIR, '/docs/api-reference', {
      title: 'API Reference',
      description: intro.split(/(?<=\.)\s/)[0],
    });
  } else {
    for (const spec of specs) {
      await generateSpec(spec, path.join(OUT_DIR, spec.id), `/docs/api-reference/${spec.id}`, {
        title: spec.doc.info?.title ?? spec.id,
        description: spec.doc.info?.description ?? '',
      });
      const meta = JSON.parse(await readFile(path.join(OUT_DIR, spec.id, 'meta.json'), 'utf8'));
      await writeFile(
        path.join(OUT_DIR, spec.id, 'meta.json'),
        JSON.stringify({ ...meta, title: spec.doc.info?.title ?? spec.id }, null, 2),
      );
    }
    await writeFile(path.join(OUT_DIR, 'meta.json'), `${JSON.stringify({ pages: specs.map((s) => s.id) }, null, 2)}\n`);
    await writeFile(path.join(OUT_DIR, 'index.mdx'), multiSpecRootIndex(specs));
  }
  log(`API reference regenerated from ${specs.length} spec${specs.length === 1 ? '' : 's'} in content/docs/api-reference`);
}

// CLI entry: `pnpm generate:api`
if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  await generateApiDocs();
}
